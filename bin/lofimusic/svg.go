type svgIcon struct {
	app.Compo

	ISize   int
	IrawSVG string
}

func newSVGIcon() *svgIcon {
	return &svgIcon{
		ISize: 24,
	}
}

func (i *svgIcon) Size(v int) *svgIcon {
	if v > 0 {
		i.ISize = v
	}
	return i
}

func (i *svgIcon) RawSVG(v string) *svgIcon {
	i.IrawSVG = v
	return i
}

func (i *svgIcon) Render() app.UI {
	size := fmt.Sprintf("%vpx", i.ISize)

	return app.Div().
		Style("width", size).
		Style("height", size).
		Body(
			app.Raw(fmt.Sprintf(i.IrawSVG, i.ISize, i.ISize)),
		)
}
